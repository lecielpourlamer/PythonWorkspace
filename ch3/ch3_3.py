number = 2 + 3 * 4
print(number)   # 14

print()
number += 2     # number = number + 2
print(number)   # 16

print()
number -= 2     # number = number - 2
print(number)   # 14

print()
number *= 2     # number = number * 2
print(number)   # 28

print()
number /= 2     # number = number / 2
print(number)   # 14.0

print()
number **= 2    # number = number ** 2
print(number)   # 196.0

print()
number //= 2    # number = number // 2
print(number)   # 98.0

print()
number %= 2     #number = number % 2
print(number)   # 0.0
